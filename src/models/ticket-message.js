function TicketMessage() {
    user: {
        this.userId = '';
        this.firmId = '';
        this.firstName = '';
        this.lastName = '';
    }
    ticket: {
        this.project = '';
        this.issueType = '';
        this.summary = '';
        this.description = '';
        this.affectedFirm = '';
        this.date = '';
        this.link = '';
        this.status = '';
    }
}
